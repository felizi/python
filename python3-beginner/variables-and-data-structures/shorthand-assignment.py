num *= 3
print(num)

print("/= Divide Equals")
num /= 2
print(num)

print("%= Mod Equals")
num %= 33333333
print(num)

print("**= Expo Equals")
num %= 55
print(num)

print("//= FloatDiv Equals")
num //= 2
